            postgraduate, doctoral, or research program may apply for this award. Students of Ph.D. candidates from
            developing nations or nations through political transformation will be given preference.
            <br>
            <br>
            Students enrolled in master's and postgraduate programs will get 850 euros per month for two years. Doctoral
            candidates and visitors to Germany for research will each get 1,100€ per month for three years. Only courses
