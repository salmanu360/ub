          <h4><b>1. DAAD Scholarship Programs</b></h4>
          <li style="list-style: none;">
            The German Intellectual Exchange Service, also known as DAAD (Deutscher Akademischer Austauschdienst), is a
            collaboration of German higher education institutions charged with fostering international academic ties. They
            concentrate on fostering educational and research collaboration by providing grants and scholarships to
            international undergraduates, graduate students, and scientists working in Germany.
            <br>
            <br>
            The DAAD offers scholarships to students who intend to pursue a degree in a full-time program of study. The
            candidates must have at least two years of verifiable job experience and a bachelor's degree earned within the
            last six years. The monthly salary for the DAAD fellowship for postgraduate and doctoral students is 850 euros
            and 1,200 euros, respectively.
          </li>
          <br>
          <br>
          <h4><b>2. Heinrich Böll Foundation Scholarships in Germany</b></h4>
          <li style="list-style: none;">
            The Heinrich Böll Foundation gives about 1,200 scholarships to deserving undergraduate, graduate, and
            doctorate students from various fields of study and nations. These students should pursue their degrees in
            public or publicly recognized German universities. Scholarship recipients are expected to have excellent
            academic records, be politically and socially active, and have a keen interest in principles such as justice,
            democracy, ecology, and self-determination.
            <br>
            <br>
            In addition to individual allowances, non-EU Masters's students will earn 850€ per month (tuition fees might
            be possible). EU students at the same academic level will earn 649 euros plus 300 euros for books each month.
            It is given for the usual study period with the option of an additional semester.
            <br>
            <br>
            On the other hand, Ph.D. students from outside the EU will earn 1,200 euros per month in addition to a 100
            euro monthly mobility allowance and other specific allowances (excluding tuition fees). EU-based Ph.D.
            candidates will get 1,350 euros per month in addition to a 100 euro research costs grant (excluding tuition
            fees). The scholarship is given for two years, with two half-year extensions possible.
          </li>
          <br>
          <br>
          <h4><b>3. DeutschlandStipendium National Scholarship Programme</b></h4>
          <li style="list-style: none;">
            The Deutschlandstipendium provides financial assistance to bright, motivated students from all around the
            world. It offers financial assistance to students at all German universities that participate without regard
            to a person's citizenship or parental or personal wealth. Through a public-private partnership, enterprises,
            foundations, or private people provide students with a monthly stipend of 150 euros, and the federal
            government matches that stipend with an additional 150 euros. Since the universities are giving out this
            prize, applicants must adhere to their institutions' conditions.
            <br>
            <br>
            The scholarship is given for two semesters, but it may alternatively be good for as long as the typical study
            term lasts. For private funding to proceed, the institution must determine after two semesters if the student
            still satisfies the requirements for the scholarship. You can also access mentoring programs, networking
            opportunities, and internships through this grant.
          </li>
