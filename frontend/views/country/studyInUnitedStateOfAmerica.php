                        <br>
                        •   Pay the SEVIS fee (Student and Exchange Visitor Information System);
                        <br>
                        •   Fill out the necessary paperwork and submit a recent photo;
                        <br>
                        •   Pay the price for applying for a visa;
                        <br>
                        •   Plan and show up for a visa interview.
