                        sector in Europe recently.
                    </p>

                    <img src="images/education-system-img.png">

                    <ul class="lpage-education-system-degree">
                        <li>
                            <b>The Bologna Process</b>
                            <br>
                            <br>
                            The 'Bologna Process' was established to standardize higher education across Europe. The
                            Italian university where the Bologna declaration was signed in 1999 by education ministers
                            from 29 European nations inspired the process's name.
                            <br>
                            <br>
                            In most European nations, the Bologna Process established a three-tiered structure of higher
                            education:
                            <br>
                            The lowest level is a bachelor's degree, typically taking three to four years to complete.
                            The next level is a master's degree, which usually takes one to two years to complete. The
                            highest level is a doctorate (Ph.D.), which is typically quite specialized and can take two
                            to five years to complete.
                            <br>
                            Following the holder's name is the degree title, such as a Bachelor of Arts (abbreviated to
                            BA) or a Master of Science (abbreviated to MSc). Typically, a doctoral holder is referred to
                            as "Dr."
                            <br>
                            <br>
                            The benefit of this method is that it is now simpler to apply for a job or course in another
                            European country using credentials from one. Moving within Europe is simpler for students
                            and job seekers thanks to improved compatibility between educational systems. The Bologna
                            changes also helped to increase the competitiveness and allure of European universities and
                            colleges to the rest of the globe.
                        </li>
                        <li>
                            <b>ECTS System</b>
                            <br>
                            <br>
                            The European Credit Transfer and Accumulation System, or ECTS, is another grading scheme
                            recognized by the system. 60 ECTS credits, or about 1500–1800 hours of total workload, are
                            equal to one academic year and allow for transfer and advancement across the European Union.
                        </li>
                    </ul>
