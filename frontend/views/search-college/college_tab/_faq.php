              <h4 class="faq-question"> 1. In Canada, what are the alternatives to post-secondary education?  </h4>
              <p class="faq-answer"> Tertiary education in Canada encompasses all types of official teaching program after secondary school, including academic, vocational, technical, and professional education, which is largely provided through universities, colleges, and special institutions. </p>




              <h4 class="faq-question"> 2. When applying to a Canadian post-secondary educational institution, what form of high school diploma is accepted? </h4>
              <p class="faq-answer"> If you're worried about whether your home nation's high school certificate will be accepted in Canada, don't be. Any student with a government-accredited high school diploma from their home country is eligible to apply for admission to a Canadian university. </p>



              <h4 class="faq-question"> 3. What are the most frequent prerequisites for admission to a Canadian university or college? </h4>
              <p class="faq-answer">Students must satisfy certain prerequisites in order to be admitted to a Canadian higher education institution, whether it is for an undergraduate or postgraduate degree. Competition varies depending on the topic of study, program, and other relevant factors. However, high school grades and performance, as well as language abilities, whether English (in most provinces) or French (in francophone provinces), are the two most important factors. </p>


              <h4 class="faq-question"> 4. After graduating from a Canadian post-secondary educational institution, what are your odds of obtaining a postgraduate degree? </h4>
              <p class="faq-answer"> You are more than welcome to continue your postgraduate studies in Canada once you have finished a BA degree at a Canadian postsecondary institution, with full legal backing from the Canadian authorities in terms of migration regulations. </p>

              <h4 class="faq-question">5.  In Canada, how long does it take to obtain a Master's degree?</h4>
              <p class="faq-answer"> Master's degrees typically last a full academic year, with a mandatory internship added on top of that. Depending on the curriculum and the sensitivity of the programme, a Master's degree can span up to two academic years. </p>


